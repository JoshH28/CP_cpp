ll dp[51][5][50], l, r;
string sigma;
ll recur(ll digit, ll bound, ll ml) {
    if (digit == 20) return 1;
    if (dp[digit][bound][ml] != -1) return dp[digit][bound][ml];
    ll maxnum = 9;
    if (bound)  maxnum = min(maxnum, (ll)(sigma[digit] -'0'));
    if (ml!=0) maxnum = min(maxnum, ml-1);
    ll ans = 0;
    for (ll q = 0; q <= maxnum; q++) {
        ll nm = ml; 
        if (ml == 0 && q != 0) nm = q;
        bool newbound = (bound && ((ll)(sigma[digit] -'0') == q));
        ans += recur(digit+1, newbound, nm);
    }
    return dp[digit][bound][ml] = ans;
}

ll hmm(ll num) {
    sigma = to_string(num);
    while (sigma.length() < 20) sigma = "0" + sigma;
    memset(dp, -1, sizeof(dp));
    return recur(0, 1, 0);
}

void solve() {
    cin >> l >> r;
    cout << hmm(r) - hmm(l-1);
}

